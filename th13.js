var counter = 5;/*
var выводить окошко для того чтобы вводить числа
*/
while (counter < 10){ // цикл while выводит все числа поочередно
    counter++;//counter ведёт запись как +1
    document.write("Counter" + counter);
    break;//строка break останавливает цикл
    document.write("Эта строка не выполнится.")// выводит на саму страницу сайта
}
